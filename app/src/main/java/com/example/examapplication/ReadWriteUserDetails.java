package com.example.examapplication;

public class ReadWriteUserDetails {
    public String email, name, phoneNo,  institute, userName, finalRole;

    ReadWriteUserDetails(){}
    ReadWriteUserDetails(String email,String name,String phoneNo, String institute, String userName, String finalRole){
        this.email=email;
        this.name=name;
        this.phoneNo=phoneNo;
        this.institute=institute;
        this.userName=userName;
        this.finalRole=finalRole;
    }

}
